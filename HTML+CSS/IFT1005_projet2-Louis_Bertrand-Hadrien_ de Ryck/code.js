function openText(text) {

    let numeroTexte = document.getElementById(text);

    if (text != "info1" && document.getElementById("info1").getAttribute("style").includes("z-index: 1;") ){
        return;
    }
    else if (text != "info2" && document.getElementById("info2").getAttribute("style").includes("z-index: 1;") ){
        return;
    }
    else if (text != "info3" && document.getElementById("info3").getAttribute("style").includes("z-index: 1;") ){
        return;
    }

    numeroTexte.setAttribute("style", "z-index: 1;");
}

function closeText(text) {
    let numeroTexte = document.getElementById(text);

    numeroTexte.setAttribute("style", "z-index: -1;");
}

function mouvText(text) {
    let text1 = document.getElementById(text + "Classe1");
    let text2 = document.getElementById(text + "Classe2");
    if(text1.getAttribute("style").includes("opacity: 1;")){
        text1.setAttribute("style", "opacity: 0;");
        text2.setAttribute("style", "opacity: 1;");
    }
    else if(text2.getAttribute("style").includes("opacity: 1;")){
        text1.setAttribute("style", "opacity: 1;");
        text2.setAttribute("style", "opacity: 0;");
    }
}